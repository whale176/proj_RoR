
(1..5).each do |i|
  puts 2 ** i
end


[1,2,3,5,6].map {|i| 2**i}
# => [2, 4, 8, 32, 64]
a = [1,2,3,4,5,6]
# => [1, 2, 3, 4, 5, 6]
a.map{|i| 2** i }
# => [2, 4, 8, 16, 32, 64]
puts a
# => [1, 2, 3, 4, 5, 6]
a.map! {|i| 2** i }
# => [2, 4, 8, 16, 32, 64]
a
# => [2, 4, 8, 16, 32, 64]
a.map(&:to_s)
# => ["2", "4", "8", "16", "32", "64"]
a.map(&:to_s) == a.map {|i| i.to_s }